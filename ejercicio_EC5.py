# Pseudocodigo
# Solicitar la nota del estudiante y guardarla en una variable llamada “nota_estudiante” 
# si la variable “nota_estudiante” es mayor a 70
#   Imprimir por consola el texto “Aprobado”
# Caso contrario:
#	Imprimir por consola el texto “Desaprobado”


# Codigo
nota_estudiante = int(input("ingrese su nota: "))

if(nota_estudiante > 70):
    print("Aprobado")
else:
    print("Desaprobado")


